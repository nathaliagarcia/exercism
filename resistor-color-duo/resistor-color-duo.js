export const decodedValue = (colors) => {
  const selectedColors = colors.slice(0, 2);
  return Number(selectedColors.map((color) => COLORS.indexOf(color)).join(""));
};

export const COLORS = [
  "black",
  "brown",
  "red",
  "orange",
  "yellow",
  "green",
  "blue",
  "violet",
  "grey",
  "white",
];
